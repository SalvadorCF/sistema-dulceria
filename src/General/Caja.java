    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        //ID VENNTA
         String sqlID = "SELECT COUNT(IdVenta) FROM Ventas";
        Statement stID;
        int idVenta = 0;
        String idV="";
        
        //Normal
        String metodoPago = (String) comboMetodo.getSelectedItem();
        
        try {
            //ID Venta
            stID = cn.createStatement();
            ResultSet rsID = stID.executeQuery(sqlID);
            rsID.next();
            idVenta = Integer.parseInt(rsID.getString(1))+1;
            idV="V"+idVenta;
            //********************************
            
            PreparedStatement pps = cn.prepareStatement("INSERT INTO Ventas values (?,?,?,?,?,?)");
            pps.setString(1, idV);
            pps.setString(2, txtFecha.getText());
            pps.setString(3, txtCliente.getText());
            pps.setString(4, txtEmpleado.getText());
            pps.setString(5, txtTotal.getText());
            pps.setString(6, metodoPago);
            pps.executeUpdate();
            
            //Agregar los detalles de ventas
            int numeroFilas = tablaProductos.getRowCount();
            String[] productos = new String[numeroFilas];
            String[] cantidad = new String[numeroFilas];
            String[] total = new String[numeroFilas];

            for (int i = 0; i < numeroFilas; i++) {
                productos[i] = tablaProductos.getValueAt(i, 0).toString();
                cantidad[i] = tablaProductos.getValueAt(i, 2).toString();
                total[i] = tablaProductos.getValueAt(i, 4).toString();

                pps = cn.prepareStatement("INSERT INTO DetalleVenta values (?,?,?,?,?)");
                pps.setString(1, idV + "D" + String.valueOf(i + 1));
                pps.setString(2, idV);
                pps.setString(3, productos[i]);
                pps.setString(4, cantidad[i]);
                pps.setString(5, total[i]);
                pps.executeUpdate();

                //Metodo de actualizar Inventario
                String sql = "SELECT Restante FROM Inventario WHERE IdLote='" + productos[i] + "'";
                Statement st;
                int restante = 0;

                st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                rs.next();
                restante = Integer.parseInt(rs.getString(1));

                int totalR = restante - Integer.parseInt(cantidad[i]);
                pps = cn.prepareStatement("UPDATE Inventario SET Restante='"
                        + String.valueOf(totalR) + "' WHERE Idlote='" + productos[i] + "'");

                pps.executeUpdate();
            }

            txtCliente.setText(null);
            txtTotal.setText(null);

            limpiarTabla();

            JOptionPane.showMessageDialog(null, "Datos Guardados");
            
     

        } catch (SQLException ex) {
            Logger.getLogger(Caja.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_btnConfirmarActionPerformed

