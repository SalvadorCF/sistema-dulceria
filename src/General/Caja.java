    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
         String sql = "SELECT Precio,Nombre FROM Productos WHERE IdProducto= (SELECT IdProducto FROM inventario WHERE idLote='" + txtProducto.getText().toString() + "')";
        Statement st;
        float precio = 0;
        String nombre="";
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            rs.next();
            precio = Float.parseFloat(rs.getString(1));
            nombre = rs.getString(2);
            
            float cantidad = Integer.parseInt(txtCantidad.getText());
            float total = precio * cantidad;
            String[] producto = new String[5];
            producto[0] = txtProducto.getText();
            producto[1] = nombre;
            producto[2] = txtCantidad.getText();
            producto[3] = String.valueOf(precio);
            producto[4] = (String) String.valueOf(total);
            modeloP.addRow(producto);

            float totalN = 0;
            int numeroFilas = tablaProductos.getRowCount();
            float[] totales = new float[numeroFilas];

            for (int i = 0; i < numeroFilas; i++) {
                totales[i] = Float.parseFloat(tablaProductos.getValueAt(i, 4).toString());
                totalN = totalN + totales[i];
            }
            txtTotal.setText(String.valueOf(totalN));
            txtProducto.setText(null);
            txtCantidad.setText(null);

        } catch (SQLException ex) {
            Logger.getLogger(Caja.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

