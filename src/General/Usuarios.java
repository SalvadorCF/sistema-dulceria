    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fila = tablaUsuarios.getSelectedRow();
        String seleccion = "";

        if (fila >= 0) {
            txtId.setEditable(false);
            txtId.setText(tablaUsuarios.getValueAt(fila, 0).toString());
            txtUsername.setText(tablaUsuarios.getValueAt(fila, 1).toString());
            txtPassword.setText(tablaUsuarios.getValueAt(fila, 2).toString());
            txtEmpleado.setText(tablaUsuarios.getValueAt(fila, 4).toString());

            seleccion = tablaUsuarios.getValueAt(fila, 3).toString();

            if (seleccion.equals("CAJERO")) {
                comboCargo.setSelectedIndex(0);
            } else if (seleccion.equals("BODEGA")) {
                comboCargo.setSelectedIndex(1);
            } else if (seleccion.equals("GERENTE")) {
                comboCargo.setSelectedIndex(2);
            } else if (seleccion.equals("GERENTE")) {
                comboCargo.setSelectedIndex(3);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pps = cn.prepareStatement("UPDATE Usuarios SET "
                    + "Username='" + txtUsername.getText() + "',"
                    + "Password='" + txtPassword.getText() + "',"
                    + "IdEmpleado='" + txtEmpleado.getText() + "',"
                    + "Cargo='" + comboCargo.getSelectedItem().toString() + "' "
                    + "WHERE IdUsuario='" + txtId.getText() + "'");

            pps.executeUpdate();

            txtId.setEditable(true);

            txtId.setText(null);
            txtId.setText(null);
            txtUsername.setText(null);
            txtPassword.setText(null);
            txtEmpleado.setText(null);
            mostrarTabla();

            JOptionPane.showMessageDialog(null, "Datos actualizados");

        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

