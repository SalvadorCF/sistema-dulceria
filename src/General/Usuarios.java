    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pps = cn.prepareStatement("INSERT INTO Usuarios VALUES(?,?,?,?,?)");
            pps.setString(1, txtId.getText());
            pps.setString(2, txtUsername.getText());
            pps.setString(3, txtPassword.getText());
            pps.setString(4, comboCargo.getSelectedItem().toString());
            pps.setString(5, txtEmpleado.getText());

            pps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Guardados");
            
            limpiarTabla();
            txtId.setText(null);
            txtUsername.setText(null);
            txtPassword.setText(null);
            txtEmpleado.setText(null);
            mostrarTabla();
        } catch (SQLException ex) {
            Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tablaUsuarios.getSelectedRow();
        String valor = tablaUsuarios.getValueAt(fila, 0).toString();
        if (fila >= 0) {
            try {
                PreparedStatement pps = cn.prepareStatement("DELETE FROM Usuarios WHERE IdUsuario='" + valor + "'");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato Eliminado");
                mostrarTabla();
            } catch (SQLException ex) {
                Logger.getLogger(Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

